// 5. Write a number to check whether a number is even or odd.

public class q5 {
    public static void main(String[] args) {
        int num = 25;
        if (num%2==0){
            System.out.println(num + " is even number");
        }
        else {
            System.out.println(num + " is odd number");
        }
    }
}
