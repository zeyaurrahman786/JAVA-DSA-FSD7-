// 3. Write a program to check whether a number is negative, positive or zero. 

public class q3 {
    public static void main(String[] args) {
        int x = 8;
        if(x>0){
            System.out.println(x + " is positive");
        }
        else if(x<0){
            System.out.println(x + " is negative");
        }
        else {
            System.out.println(x + " is zero");
        }
    }
}
