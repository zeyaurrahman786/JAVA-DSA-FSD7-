

public class RelationOperator {
    public static void main(String[] args) {
        int a = 42;
        int b = 36;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a==b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);
    }
}
